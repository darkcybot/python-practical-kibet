numbers = [4, 8, 1, 9, 3]

print("len():", len(numbers))          # count of items
print("max():", max(numbers))          # largest value
print("min():", min(numbers))          # smallest value
print("sum():", sum(numbers))          # total of all values
print("type():", type(numbers))        # data type
print("sorted():", sorted(numbers))    # sorted list
print("abs():", abs(-15))              # absolute value
print("round():", round(3.14159, 2))   # rounded number
print("str():", str(123))              # convert to string
print("int():", int("50"))             # convert to integer